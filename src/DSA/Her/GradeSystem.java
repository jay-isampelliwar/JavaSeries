package Her;

public class GradeSystem {

    public static void main(String args []) {


        int marks = 97;

        if( marks >= 90) {

            System.out.println("Grade A+");
        }
        else if (marks >= 80) {

            System.out.println("Grade A");
        }
        else if (marks >= 70) {

            System.out.println("grade B");
        }
        else if (marks >= 60) {

            System.out.println("Grade C");
        }
        else if ( marks >= 50) {

            System.out.println("Grade D");
        }
        else {

            System.out.println("Grade E");
        }

    }
}
